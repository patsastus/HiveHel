#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	ret;
	int	sign;

	sign = 1;
	while (*nptr == 32 || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	ret = 0;
	while (ft_isdigit(*nptr))
	{
		ret *= 10;
		ret += *nptr - '0';
		nptr++;
	}
	return (ret * sign);
}
